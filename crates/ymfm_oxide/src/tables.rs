// OPL KSL: top 4 bits of FNUM to attenuation, maximal values for block == 7.
// Values for other blocks are computed by subtracting 8 for each block below 7.
static FNUM_TO_ATTEN: [u8; 16] = [
    0, 24, 32, 37, 40, 43, 45, 47, 48, 50, 51, 52, 53, 54, 55, 56,
];

pub(crate) fn opl_key_scale_atten(block: u32, fnum_4msb: u32) -> u32 {
    let result = FNUM_TO_ATTEN[fnum_4msb as usize] as i32 - 8 * ((block ^ 7) as i32);
    if result > 0 { result as u32 } else { 0 }
}

