// Given a 5.8 fixed point logarithmic attenuation value, return a 13-bit
// linear volume. Looks up the fractional part, then shifts by the whole.
pub(crate) fn attenuation_to_volume(input: u32) -> u32 {
    (POWER_TABLE[(input & 0xFF) as usize] as u32) >> (input >> 8)
}

