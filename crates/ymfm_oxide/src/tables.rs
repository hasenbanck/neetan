// Given a sin (phase) input where the range 0..2*PI is mapped onto 10 bits,
// return the absolute value of sin(input), logarithmically-adjusted and
// treated as an attenuation value, in 4.8 fixed point format.
pub(crate) fn abs_sin_attenuation(input: u32) -> u32 {
    let mut input = input;
    // If the top bit is set, we're in the second half of the curve
    // which is a mirror image, so invert the index.
    if crate::helpers::bit(input, 8) != 0 {
        input = !input;
    }
    SIN_TABLE[(input & 0xFF) as usize] as u32
}

