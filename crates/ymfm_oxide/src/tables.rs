// Given the 7 most significant frequency number bits, plus a 3-bit PM depth
// value and a signed 5-bit raw PM value, return a signed PM adjustment to
// the frequency. Algorithm written to match Nuked behavior.
pub(crate) fn opn_lfo_pm_phase_adjustment(
    fnum_bits: u32,
    pm_sensitivity: u32,
    lfo_raw_pm: i32,
) -> i32 {
    let abs_pm = if lfo_raw_pm < 0 {
        -lfo_raw_pm
    } else {
        lfo_raw_pm
    };
    let shifts = LFO_PM_SHIFTS[pm_sensitivity as usize]
        [crate::helpers::bitfield(abs_pm as u32, 0, 3) as usize];
    // Compute the adjustment: two shifts summed as a cheap multiply.
    let mut adjust = (fnum_bits >> crate::helpers::bitfield(shifts as u32, 0, 4)) as i32
        + (fnum_bits >> crate::helpers::bitfield(shifts as u32, 4, 4)) as i32;
    if pm_sensitivity > 5 {
        adjust <<= pm_sensitivity - 5;
    }
    adjust >>= 2;
    // Every 16 cycles it inverts sign.
    if lfo_raw_pm < 0 { -adjust } else { adjust }
}

